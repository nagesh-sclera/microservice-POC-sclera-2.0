package io.sclera.assetonboardingai.config;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.cloud.client.loadbalancer.LoadBalanced;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.reactive.function.client.WebClient;

@Configuration
public class WebClientConfig {

    @Bean
    @LoadBalanced
    public WebClient.Builder webClientBuilder() {
        return WebClient.builder();
    }

    @Bean("aiWebClient")
    public WebClient aiWebClient(@Value("${ai.external-endpoint}") String aiEndpoint) {
        // uses its own fresh builder so it bypasses load-balancer resolution
        return WebClient.builder().baseUrl(aiEndpoint).build();
    }
}
